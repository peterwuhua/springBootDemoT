package cn.demi.app.appCyd.vo;

public class AppAppara {
	/**
	 * 现场分析记录单 采样仪器
	 */
	private String id;
	private String no;
	private String name;
	private String spec;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSpec() {
		return spec;
	}

	public void setSpec(String spec) {
		this.spec = spec;
	}

}
